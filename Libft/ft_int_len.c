#include "libft.h"

int		ft_int_len(int n)
{
	int len;
	int	zero;

	zero = 1;
	len = 0;
	if (n == 0)
		return (zero);
	while (n != 0)
	{
		len++;
		n /= 10;
	}
	return (len);
}
